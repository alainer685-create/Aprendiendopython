# Con if
numero = int(input("Ingresa un número: "))

if numero % 2 == 0:
    print("El número es par.")
else:
    print("El número es impar")   

# Con for
print("Contando con for: ")
for num in range(1, 11):
    print(num)     

# Con while
print("Contando con while:")
contador = 1
while contador <= 10:
    print(contador)
    contador += 1    
